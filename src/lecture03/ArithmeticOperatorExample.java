package lecture03;

public class ArithmeticOperatorExample {

	public static void main(String[] args) {
		int v1 = 5;
		int v2 = 2;
		
		int result1 = v1 + v2;
		System.out.println("resert1=" +result1);
		
		int result2 = v1 - v2;
		System.out.println("resert2=" +result2);
		
		int result3 = v1 * v2;
		System.out.println("resert3=" +result3);
		
		int result4 = v1 / v2;
		System.out.println("resert4=" +result4);
		
		int result5 = v1 % v2;
		System.out.println("resert5=" +result5);
		
		double result6 = (double) v1 / v2;
		System.out.println("resert6=" +result6);

	}

}
